package com.a159;

public class Op2Ex 
{
	public static void main(String[] args) {
		
		
		int a = 6;
		int b = 3;
		
		a+=b;// a = a+b;
		System.out.println(a);//9
		
		a-=b;//a=a-b
		System.out.println(a);//6
		
		a*=b;//a=a*b
		System.out.println(a);//18
		
		a/=b;//a=a/b
		System.out.println(a);
		
	
		
	}
}
